			<h4>Is it possible to meet the person who we bought the gift for?</strong> </p>
			<p>The shelter staff are the best people to make this determination. If you call and ask about the specific person, they can advise you about the possibility of making this happen. </p>
			<hr>

			<h4>Can I deliver gifts after the holidays? </h4>
			<p>You can always make donations to the shelter, anytime but because the population is quite transient, it’s possible the intended recipient may not be there.</p>
			<p>However, when you take your gift, include a label on the outside with a description of the gift (size, male or female, type of gift) they will give it to another appropriate person if the intended recipient is not there. </p>

			<hr>
			<h4>What is the homelesspartners website?</h4>
			<p>On these pages you will find the personal stories of many homeless people from several cities. The homeless are real people, with real lives, real feelings, and real hopes and dreams. By clicking on a city you can read their stories and choose one or more people to send a personal, caring gift or message by delivering it to the local shelter. Giving money directly to them may not be the most helpful, but a caring gift or message goes a long way when they realize that someone who knows only their brief story cares enough to let them know. When the homeless feel cared about, they are able to care about themselves and have courage to move forward in their lives. Not only will they feel encouraged, it will help you feel good too! </p>

			<hr>
			<h4>Who are the homeless?</h4>
			<p>They are men and women who have lost their homes and families. They are brothers and sisters, sons and daughters, fathers and mothers, aunts and uncles, nieces and nephews. If they were your family members, regardless of their circumstances or choices, you would want them to know you care. We believe everyone is our family member. Each one has a real life story with real reasons for their homelessness. Sometimes they are recently homeless. Sometimes they have lived on and off the street and in shelters for many years. Some are the working poor who don't earn enough to afford rent and rely on temporary shelters and agencies to make ends meet. Many of the homeless suffer from mental illness. This can lead to drug addiction as they try to help themselves feel better and cope with their homeless condition. Although they sometimes look able-bodied, mental illness is not always outwardly apparent. It prevents people from holding down a job as much as a physical handicap prevents a wheelchair-bound person from walking. No one deserves to suffer as the homeless do. In desperation, they often go hungry and sleep in the cold and rain. Sometimes they are able to find a bed or meal in a shelter but on the street, they are often beaten, robbed, diseased, and dying. </p>

			<hr>
			<h4>What do homeless people need?</h4>
			<p>The homeless do not have extravagant needs. One of the most pervasive problems they face, especially in winter, is hypothermia. They need winter coats and footwear, thermal underwear, warm dry socks, gloves, toques, and scarves. Some who are capable of working, especially in the building trades, are not able to work because they have no tools, tool belt or steel-toe work boots. These can be donated new or used as long as they are in sturdy and reusable condition. Many homeless people are talented artists, musicians, and writers. By gifting art supplies like canvasses, acrylic paints, brushes, etc. they can be afforded the tools to create works of art.</p>
			 
