			<h4>Can I donate to the program so that, after all of the pledges are made, if anyone hasn't received their request, the money I send can be used to help them?</h4>
			Yes, in most cities, we have a buyer who will carefully examine the list to try to ensure that everyone on the list gets something.</p>
