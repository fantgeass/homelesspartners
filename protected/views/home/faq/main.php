			<h2>Frequently Asked Questions</h2>

			<hr>

			<h4>How does it work?</h4>
			<p>You go to the website and follow the steps.</p>
			<ol>
			<li>You choose a city and shelter near you,</li>
			<li>Then read the stories of people who stay at shelters.</li>
			<li>Then pledge a gift for as many people as you choose.</li>
			<li>Then you get the gift(s) and deliver them to the shelter with a caring note for each person. </li>
			</ol>
			<p>Voila! Harry has the new blue coat he asked for, in his size, with a caring note so he knows that someone cares about him. </p>
			<p>It’s all about connection – helping the homeless know that someone in the community cares. </p>

			<hr>


			<h4>How do I know if the person received the gift? </h4>
			<p>You can call the shelter and ask them about a specific person. </p>
			<p>This is an aspect we are trying hard to improve. We would like to offer the gift recipients an option to write a thank you note and we are trying to figure out a way to gather the thank you notes so that they can be searched by the name, gift and ID #. </p>
			<p>It’s a challenge that we’re trying to address. </p>


			<hr>

			<h4>How is homelesspartners organized? </h4>
			<p>Since 2005, homelesspartners has been conducted entirely by volunteers. Different organizations (mostly churches, but sometimes other groups) coordinate volunteers in their city to go into shelters in late November or early December and personally interview the shelter residents, asking them a little about their life – whatever they feel comfortable sharing – as well as items for their Christmas wish list. These volunteers then post the interview + the wish list on the website: homelesspartners. </p>
			<p>Other volunteers send out a press release to the media (radio, TV and newspaper)+ put it on social media (facebook, twitter and emails) so that people in the community find out about the website, read the stories, buy the gifts and deliver them to the shelters. </p>
			<p>The result: the shelter residents receive a gift, just for them, and they know that someone cares about them as an individual. </p>

			<hr>

			<h4>How is homelesspartners funded? </h4>
			Since 2005 homelesspartners has not been an official organization – only groups of people in different cities who want to help the homeless. None of the volunteers have been salaried and they have not received any donations as an overall organization. Sometimes individuals have made donations through the specific city organization that is coordinating the effort and sometimes people make donations directly to shelters. </p>
			<p>This year, homelesspartners is taking a major growth step. It has become federally incorporated as a non-profit organization with a view toward becoming a registered charity. </p>
			<p>In October-November of 2013, the first fundraising campaign was undertaken through indiegogo and we successfully raised $10,000. These funds will go towards expenses for volunteer programmers to create a new website – their food and resource expenses, as well as accounting costs and registration fees. </p>
			<p>The new website is designed to solicit the enrollment of new cities, so there may be a part time staff position (for Nov-Dec) created to accommodate the increase in administrative needs to enroll additional cities. </p>
			<p>Also, funds will be forwarded to each participating city so that a “buyer” in that city can purchase individual gifts for people who are listed on the site who may have been ungifted. One of the purposes is to ensure that each person on the list receives something. </p>
			<p>All funds collected with be absolutely, transparently, available to anyone. </p>

