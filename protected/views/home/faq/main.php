			<h4>Can I make a donation directly to homelesspartners? </h4>
			<p>Yes, for the first time, homelesspartners is <a href="http://homelesspartners.com/donate">accepting donations</a>. These funds will be used for: accounting and taxation fees, for a part time worker during Nov –December, for software expenses and made available to participating city coordinators who will oversee the purchase of gifts for those listed on the site.</p> 
			<p>All funds will be open and transparent to anyone. </p>

