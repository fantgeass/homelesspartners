        <h3>Connecting People</h3>
        <p>Homeless Partners is a non-profit program run entirely by volunteer efforts, in partnership with the staff at shelters where the program is offered. The purpose of the Homeless Partners Wish List is to help people connect personally and directly with individual homeless people in their own city in a way that is safe and practical.</p>

        <p>Poverty is relationship based. Relationships with people, spiritual relationships, and the environment. We believe that a powerful way to help the homeless is to know their stories and to connect with them person-to-person. This connection builds strength and courage to move forward in their life. This homeless partners platform acts as a tool to help facilitate this change in your community, and around the world. 
